<script>
export const listViewTab = 0;
export const wishlistTab = 1;
export const legalNotiveTab = 2;

export const singleGameViewTab = 1;


import GamesList from './components/GamesList.vue';
import SingleGame from './components/SingleGame.vue';
import WishList from './components/WishList.vue';
import axios from 'axios';
import Cookies from 'js-cookie';

export default {
    created() {
        this.fetchGamesList()
    },
    components: {
        GamesList, SingleGame, WishList
    },
    methods: {
        setTab(value) {
            this.tab = value;
        },
        fetchGamesList() {
            /*
             * Headers in options generated by 'rapidapi.com/digiwalls/api/mmo-games' 
             */
            const options = {
                method: 'GET',
                url: 'https://mmo-games.p.rapidapi.com/games',
                headers: {
                    'X-RapidAPI-Key': '6875f49c94msh53fa1e984a8faf3p1a30f2jsn6b0d9ecd7404',
                    'X-RapidAPI-Host': 'mmo-games.p.rapidapi.com'
                }
            }

            axios.request(options)
                .then(response => {
                    console.log(response.data)
                    this.gamesArray = response.data
                })
                .catch(console.error);
        },
        showGame(gameId) {
            this.findGame(gameId);
            this.selectedView = singleGameViewTab;
        },
        findGame(gameId) {
            this.selectedGameIndex = 0
            while (gameId != this.gamesArray[this.selectedGameIndex].id) {
                this.selectedGameIndex++;
            }
        },
        showList() {
            this.selectedView = listViewTab
        },
        addToWishlist(gameId){
            this.findGame(gameId);
            this.wishlist.push(this.selectedGameIndex);
            Cookies.set('wishlist', JSON.stringify(this.wishlist));
        },

        removeFromWishlist(gameId){
            this.findGame(gameId)
            this.wishlist.splice(this.selectedGameIndex);
            this.$cookie.set('wishlist', JSON.stringify(this.wishlist));
        },

        restoreWishlistFromCookies(){
            const cookieWishlist = Cookies.get('wishlist');
            if(cookieWishlist){
                this.wishlist = JSON.parse(cookieWishlist);
            }
        }
    },
    data() {
        return {
            tab: listViewTab,
            gamesArray: [],
            selectedView: listViewTab,
            selectedGameIndex: 0,
            wishlist: [],
        }
    }
}

</script>

<template>
    <div id="frame">
        <h1>Computer Games Wishlist</h1>
        <v-card>
            <v-tabs fixed-tabs v-model="tab" bg-color="blue">
                <v-tab :value="0">All Games</v-tab>
                <v-tab :value="1">Wishlist</v-tab>
                <v-tab :value="2">Legal Notice</v-tab>
            </v-tabs>
            <v-window v-model="tab">
                <v-window-item :value="0">
                    <div v-show="selectedView == 0">
                        <GamesList :games="gamesArray" @viewGame="showGame"></GamesList>
                    </div>
                    <div v-show="selectedView == 1">
                        <SingleGame @returnToList="showList" :gameIndex="selectedGameIndex" :games="gamesArray">
                        </SingleGame>
                    </div>
                </v-window-item>
                <v-window-item :value="1">
                    <WishList></WishList>
